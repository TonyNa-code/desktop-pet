  function normalizeLanguage(value) {
    if (value === "system" || SUPPORTED_LANGUAGES.includes(value)) return value;
    return "system";
  }

  function languageFromLocale(locale) {
    const cleanLocale = String(locale || "").toLowerCase();
    if (cleanLocale.startsWith("ja")) return "ja-JP";
    if (cleanLocale.startsWith("en")) return "en-US";
    if (cleanLocale.startsWith("zh")) return "zh-CN";
    return FALLBACK_LANGUAGE;
  }

  function resolveLanguage(language, systemLocale) {
    const normalized = normalizeLanguage(language);
    if (normalized === "system") return languageFromLocale(systemLocale);
    return normalized;
  }

  function dictionaryFor(language) {
    return DICTIONARIES[language] || DICTIONARIES[FALLBACK_LANGUAGE];
  }

  function t(key, variables = {}, language = FALLBACK_LANGUAGE) {
    const dictionary = dictionaryFor(language);
    const fallbackDictionary = dictionaryFor(FALLBACK_LANGUAGE);
    const template = dictionary[key] || fallbackDictionary[key] || key;
    return String(template).replace(/\{([a-zA-Z0-9_]+)\}/g, (_match, name) => (
      variables[name] === undefined || variables[name] === null ? "" : String(variables[name])
    ));
  }

  function languageLabel(value, language = FALLBACK_LANGUAGE) {
    const option = LANGUAGE_OPTIONS.find((item) => item.value === value);
    return option ? t(option.labelKey, {}, language) : String(value);
  }

  return {
    FALLBACK_LANGUAGE,
    SUPPORTED_LANGUAGES,
    LANGUAGE_OPTIONS,
    normalizeLanguage,
    languageFromLocale,
    resolveLanguage,
    t,
    languageLabel,
  };
